<div>
	<h1>Aide du plugin Static MiniForum</h1>
	<h2>Afficher un forum</h2>
	<p>Dans la config du plugin, séléctionner la ou les pages statiques où vous voulez voir apparaitre un mini-forum.</p>
	<p>Vous pouvez créer une page statique uniquement dédié à l'insertion d'un forum sur votre site.</p>
	<h2>Aide à la configuration</h2>
	<p>Dans la page configuration, cliquer sur le lien/icone <em>Wizard</em> et suivez les explications.</p>
	<h2>Modération</h2>
	<p>Pour fermer un forum, décoché la case "afficher formulaire", le forum sera toujours visible, mais sans formulaire.</p>
	<p>Pour Retirer un forum, décoché le nom de la page statique concernée.</p>
	<p>Pour effacer une réponse:</p>
	<ol>
		<li> Se connecter en Administrateur à l'administration</li>
		<li> Se rendre sur la page statique coté visiteur, ouvrir le sujet et cliquer sur le(s) bouton(s) <span style="color:red">[supprimer #X]</span> de(s) réponse(s) à effacer</li>
	</ol>
	<p>Pour effacer un sujet:</p>
	<ol>
		<li> Se connecter en Administrateur à l'administration</li>
		<li> Se rendre sur la page statique coté visiteur, cliquer sur le(s) bouton(s) 
			<span style="color:red">[supprimer #X]</span> de(s) sujet(s) à effacer
			<br>
			<Note:</b> Pour effacer un sujet, il doit être vide de réponse.
		</li>
	</ol>
	<p>Hook du widget >aucun</p>
